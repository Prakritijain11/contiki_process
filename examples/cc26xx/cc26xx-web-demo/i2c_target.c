/*---------------------------------------------------------------------------*/
void
I2CTarget_Close()
{
	board_i2c_shutdown();
}

/*---------------------------------------------------------------------------*/
bool
I2CTarget_Write(uint8_t *pu8_data, uint8_t u8_len)
{
	bool b_res;

	b_res = board_i2c_write(pu8_data, u8_len);

	return b_res;
}

/*---------------------------------------------------------------------------*/
bool
I2CTarget_Read(uint8_t *pu8_data, uint8_t u8_len)
{
	 bool b_res;

	 b_res = board_i2c_read( pu8_data, u8_len);

	 return b_res;
}
/*---------------------------------------------------------------------------*/
bool
I2CTarget_WriteRead(uint8_t *pu8_wData, uint8_t u8_wlen, uint8_t *pu8_rdata, uint8_t u8_rlen)
{
	bool b_res;

	b_res = board_i2c_write_read( pu8_wData, u8_wlen, pu8_rdata, u8_rlen );

	return b_res;
}
/*---------------------------------------------------------------------------*/
void
I2CTarget_Open(uint8_t u8_new_interface, uint8_t u8_address)
{
	board_i2c_select( u8_new_interface, u8_address);
}

/*---------------------------------------------------------------------------*/
void
I2CTarget_Wakeup( void )
{
	board_i2c_wakeup();
}

